    public void rotationDidChange(float x, float y, float z) {
        float xAngle= angle(x);
        float yAngle= angle(y);
        float zAngle= angle(z);
        
        setAngleValues(xAngle, yAngle, zAngle);
    }
    
    private void setAngleValues(float x, float y, float z){
        xRotationField.setText(String.valueOf(x));
        yRotationField.setText(String.valueOf(y));
        zRotationField.setText(String.valueOf(z));
    }
    
    private float angle(float pAngle){
        if(radians)
            return pAngle;
        
        return (float) Math.toDegrees(pAngle);
    }
    
    private boolean radians;
