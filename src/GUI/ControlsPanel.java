    private void updateRotationData(){
        if(getControlsDelegate()!=null){
            float x= valueFromField(xRotationField);
            float y= valueFromField(yRotationField);
            float z= valueFromField(zRotationField);
            getControlsDelegate().didChangeRotation(x, y, z);
        }
    }
    
    public float valueFromField(JTextField pField){
        float value= 0f;
        try{
            value= Float.parseFloat(pField.getText());
        }catch(NumberFormatException ex){
            value= 0f;
        }
        
        return value;
    }
    
